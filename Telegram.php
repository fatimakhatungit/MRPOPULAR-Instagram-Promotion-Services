<p>The story of the creation of the project could be a movie plot. The founder of the project, Pavel Durov, who had created VK, the number one social network in Russia, told in an interview how the idea of creating an ultra-secure messenger had arisen. According to Pavel himself, at that time, and the year was 2011, he had some problems with the authorities. And after another visit of their representatives, he had to urgently send a message to his brother.</p>
<p>Considering the fact that the authorities really love reading messages of their citizens, the future founder of Telegram thought of a new messenger, which would not let them do this. Fortunately, the aforementioned Pavel’s brother Nikolay was an excellent programmer. So on August 14, 2013, the cross-platform instant messenger so popular today came into existence. Of course, promotion services were not relevant at the time of the birth of the service. But users liked the idea of privacy so much that the audience began to grow at an incredible pace and soon telegram channel promotion services became a vital necessity for many.</p>
