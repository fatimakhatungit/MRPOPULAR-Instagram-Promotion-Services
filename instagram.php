<p>First off, let's rejoice with all the supporters of promotion through social networks - the number of our supporters has significantly increased in the report. So, 45% of all the surveyed companies called the cost of promotion through SMM channels effective and 29% called this process “extremely effective”. Over the year, these speaking for itself figures grew by 25-30%, which is very good news. However, the achievements of Instagram are the most interesting thing here, there is something you can really be surprised about. Among the companies surveyed, 70% of them use it as a means of promoting their product.</p> <p>If you compare it to 2017, this is a 20% increase which is an impressive year over year figure. Instagram promotion services grows at a rapid pace not only because Instagram is simple and cool. From the same report, it is clear that 85% of companies prefer video content in their SMM advertising. Could this be the reason for the urgent reformatting of Instagram in the direction of short videos, the so-called Stories? <i>There is no time to understand which one was the first and which one was the second, it’s better to dig deeper into this and other planned features in order to understand how they can help you in promotion of your Instagram account.</i></p>
